#include "minishell.h"

int	c_pr(char c)
{
	if (c == '|' || c == '<' || c == '>')
		return (1);
	return (0);
}

int	check_sp(char c)
{
	if (c == ' ' || c == '\t' || c == '\v')
		return (1);
	else
		return (0);
}
